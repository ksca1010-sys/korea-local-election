def build():
    output = {
        '_meta': {
            'description': '광역의원 현직의원 데이터 (2022 지방선거 당선인 기준)',
            'source': '각 시도의회 홈페이지 + 나무위키',
            'electionName': '제8회 전국동시지방선거 (2022.06.01)',
            'generatedAt': datetime.now().isoformat(),
        },
        'regions': {}
    }

    total_matched = 0
    total_unmatched = 0

    for region_key, members in RAW_DATA.items():
        topo_districts = load_topo_districts(region_key)
        print(f'\n[{region_key}] TopoJSON: {len(topo_districts)}개, 수집: {len(members)}명')

        district_members = {}
        unmatched = []

        for m in members:
            matched = match_district(m['district'], topo_districts)
            if matched:
                if matched not in district_members:
                    district_members[matched] = []
                district_members[matched].append({
                    'name': m['name'],
                    'party': normalize_party(m['party']),
                    'partyName': m['party'],
                })
            else:
                unmatched.append(m)

        if unmatched:
            print(f'  ⚠️  미매칭 {len(unmatched)}명:')
            for u in unmatched[:10]:
                print(f'     {u["district"]} - {u["name"]}')

        output['regions'][region_key] = {
            'districtMembers': district_members,
            'totalMembers': sum(len(v) for v in district_members.values()),
            'totalDistricts': len(district_members),
        }

        total_matched += sum(len(v) for v in district_members.values())
        total_unmatched += len(unmatched)
        print(f'  ✅ {output["regions"][region_key]["totalMembers"]}명 매칭 / {len(district_members)}개 선거구')

    output['_meta']['totalMembers'] = total_matched
    output['_meta']['unmatchedMembers'] = total_unmatched

    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(output, f, ensure_ascii=False, indent=2)
    print(f'\n💾 저장: {OUTPUT_FILE}')
    print(f'   총 {total_matched}명 매칭, {total_unmatched}명 미매칭')


if __name__ == '__main__':
    build()
