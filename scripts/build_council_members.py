def normalize_party(name):
    return PARTY_KEY_MAP.get(name.strip(), 'other')

def load_topo_districts(region_key):
    topo_file = TOPO_DIR / f'council_districts_{region_key}_topo.json'
    if not topo_file.exists():
        return []
    with open(topo_file, encoding='utf-8') as f:
        topo = json.load(f)
    obj_key = list(topo['objects'].keys())[0]
    return [
        g['properties']['district_name']
        for g in topo['objects'][obj_key]['geometries']
        if 'district_name' in g.get('properties', {})
    ]

def normalize_district(name):
    """선거구명 정규화"""
    name = re.sub(r'\s+', ' ', name.strip())
    # "강남구1" → "강남구 제1선거구"
    m = re.match(r'^(.+?)(\d+)$', name)
    if m:
        name = f'{m.group(1)} 제{m.group(2)}선거구'
    # "종로구제1선거구" → "종로구 제1선거구"
    name = re.sub(r'(\S[구시군])(제?\d+선거구)', r'\1 \2', name)
    # "종로구 1선거구" → "종로구 제1선거구"
    name = re.sub(r'(\s)(\d+선거구)', r'\1제\2', name)
    return name

def match_district(api_name, topo_names):
    normalized = normalize_district(api_name)
    if normalized in topo_names:
        return normalized
    for tn in topo_names:
        if normalize_district(tn) == normalized:
            return tn
    # 부분 매칭
    m = re.match(r'(.+?)\s*제?(\d+)\s*선거구', normalized)
    if m:
        base, num = m.group(1), m.group(2)
        for tn in topo_names:
            if base in tn and f'제{num}선거구' in tn:
                return tn
    # 단일 선거구 (군위군 → 군위군 선거구)
    base = normalized.replace(' 선거구', '').strip()
    for tn in topo_names:
        if tn.startswith(base) and '선거구' in tn:
            return tn
    # "보은군" → "보은군 선거구"
    if not normalized.endswith('선거구'):
        candidate = normalized + ' 선거구'
        if candidate in topo_names:
            return candidate
    return None


